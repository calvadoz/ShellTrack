function measurementsFromRecords(
  petId: string,
  idGroup: string,
  records: string,
): Measurement[] {
  return records
    .trim()
    .split("\n")
    .map((row, index) => {
      const [measuredAt, weight] = row.trim().split(/\s+/);
      const timestamp = `${measuredAt}T12:00:00.000Z`;
      return {
        id: `00000000-0000-${idGroup}-8000-${String(index + 1).padStart(12, "0")}`,
        petId,
        measuredAt,
        weightGram: Number(weight),
        createdAt: timestamp,
        updatedAt: timestamp,
      };
    });
}

